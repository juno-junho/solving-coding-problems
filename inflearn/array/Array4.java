import java.util.Scanner;


// 다시 풀어보기.
class Array4 {
    public int[] solution(int n) {
        int[] answer = new int[n];
        return answer;
    }

    public static void main(String[] args) {
        Array4 T = new Array4();
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        for (int i : T.solution(n)) System.out.print(i + " ");
    }
}
